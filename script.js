
function anwser1(num, correct) {
    const b1 = document.element = document.getElementById("b1");
    const b2 = document.element = document.getElementById("b2");  
    const b3 = document.element = document.getElementById("b3");  
    b1.setAttribute("disabled", true);
    b2.setAttribute("disabled", true);
    b3.setAttribute("disabled", true);

    if (correct == true) {
        var reply = document.createElement("p");
        reply.innerHTML = "Ответ на вопрос " + num + " <strong>верный!</strong>";
        reply.style.color = "green";
        var parent = document.getElementById("replydiv");
        
        
        parent.appendChild(reply);

    }
    else {
        const button = document.getElementsByClassName("buttons1"); 
        var reply = document.createElement("p");
        reply.innerHTML = "Ответ на вопрос " + num + " <strong>не верный...</strong>";
        reply.style.color = "red";
        var parent = document.getElementById("replydiv");
        
    
        parent.appendChild(reply);

    }
}
